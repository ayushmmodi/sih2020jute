      <h3 style="font-weight: bolder;">Jute Cultivation in India: Conditions, Method, Production and Distribution!</h3> <br>

      <p class="leading-relaxed text-lg" style="text-align: left;">Next to cotton, jute is the second important fibre crop of India. Jute is in great demand because of the cheapness, softness, strength, length, lustre and uniformity of its fibre.</p>
      <p class="leading-relaxed text-lg" style="text-align: left;">It is used for manufacturing a large variety of articles such as gunny bags, hessian, ropes, strings, carpets, rugs and clothes, tarpaulins, upholstery and decoration pieces.</p>
      <br>
      <br>

      <h3 style="font-weight: bolder;">Conditions of Growth:</h3> <br>

      <p class="leading-relaxed text-lg" style="text-align: left;">Jute is the crop of hot and humid climate. It requires high temperature varying from 24°C to 35°C and heavy rainfall of 120 to 150 cm with 80 to 90 per cent relative humidity during the period of its growth Small amount of pre-monsoon rainfall varying from 25 cm to 55 cm is very useful because it helps in the proper growth of the plant till the arrival of the proper monsoon. Incessant and untimely rainfalls as well as prolonged droughts are detrimental to this crop. Rainfall between 2.5 to 7.5 cm in a month, during the sowing period, is considered to be sufficient.</p>

      <p class="leading-relaxed text-lg" style="text-align: left;">Occasional showers varying from 2 to 3 cm at intervals of a week’s time during the growing period are very useful. Large quantity of water is required not only for growing the jute crop but also for processing the fibre after the crop is harvested.</p>

      <p class="leading-relaxed text-lg" style="text-align: left;">Light sandy or clayey loams are considered to be best suited soils for jute. Since jute rapidly exhausts the fertility of soil, it is necessary that the soil is replenished annually by the silt-laden flood water of the rivers. Large supply of cheap labour is also necessary for growing and processing the jute fibre.</p>

        <br>
        <br>
      
      <h3 style="font-weight: bolder;">Method of Cultivating and Processing of Jute:</h3> <br>

      <p class="leading-relaxed text-lg" style="text-align: left;">Jute is generally sown in February on lowlands and in March-May on uplands. The crop takes 8- 10 months to mature but different varieties take different time to mature. The harvesting period generally starts in July and continues till October.</p>

            <p class="leading-relaxed text-lg" style="text-align: left;">The plants are cut to the ground and tied into bundles. Sheafs of jute stocks are then immersed in flood water or ponds or stagnant water for about 2 to 3 weeks for retting. High temperature of water quickens the process of retting. After retting is complete, the bark is peeled from the plant and fibre is removed.<br>

After this, stripping, rinsing, washing and cleaning is done and the fibre is dried in the sun and pressed into bales. All this process is to be done by human hand for which/availability of plenty of labour at cheap rates is very essential. Luckily, this labour is readily available because jute is cultivated in areas of high population density.</p>





